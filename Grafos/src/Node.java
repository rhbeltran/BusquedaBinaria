public class Node{
private int vertex;
private Node next;
public Node() {
    this.vertex = 0;
    this.next = null;
}
public int getVertex() {
    return vertex;
}
public void setVertex(int vertex) {
    this.vertex = vertex;
}
public Node getNext() {
    return next;
}
public void setNext(Node next) {
    this.next = next;
}



}